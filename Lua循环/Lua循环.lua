-- [[Lua 循环]]--
-- 很多情况下我们需要做一些有规律的重复操作，因此在程序中就需要重复执行某些语句?
-- Lua语言提供了以下几种循环处理方式：
-- while 循环               在条件为true时，让程序重复执行某些语?
-- for 循环                 重复执行指定语句，重复次数可在for语句中控?
-- repeat...until           重复执行循环，直到指定条件为真为?
-- 嵌套 循环                 可以在循环内嵌套一个或者多个循环语句（while do...end;for...do...end;repeat...until;?
