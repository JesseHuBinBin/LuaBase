--[[
    Table 操作
--]]
-- 1.table.concat(table[,sep[,start[,end]]])
-- concat 是 concatenate(连锁，连接)的缩写.table.concat()函数列出参数中指定table的数组部分从start位置到end位置的所有元素，元素间以指定的分隔符（sep）隔开。

-- 2.table.insert(table,[pos,]value)
-- 在table的数组部分指定位置（pos）插入值为value的一个元素，pos参数可选，默认为数组的部分末尾。

-- 3.table.maxn(table)
-- 指定table中所有整数key值中最大的key值，如果不存在key值为正数的元素，则返回0。

-- 4.table.remove(table[,pos])
-- 返回table数组部分位于pos位置的元素，其后的元素会被前移，pos参数可选，默认为table长度，即从最后一个元素删起。

-- 5.table.sort(table[,comp])
-- 对给定的table进行升序排序。
